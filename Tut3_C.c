// IN THIS PROGRAM WE WILL LEARN ABOUT AIRTHMATIC OPERATORS IN C.
#include <stdio.h>

int main()
{
    int a,b;
    printf("ENTER THE 1st NUMBER:");
    scanf("%d",&a);
       

    printf("ENTER THE 2nd NUMBER:");
    scanf("%d",&b);
    

    //USING THE AIRTHMATIC OPERATORS IN C.
    //EXAMPLE OF AIRTHMATIC OPERATORS ARE +,-,*,/.
    printf("ADDITON: %d\n",a+b);
    printf("SUBTRACT: %d\n",a-b);
    printf("MULTIPLY: %d\n",a*b);
    printf("DIVIDE: %d\n",a/b);

    return 0;
}
